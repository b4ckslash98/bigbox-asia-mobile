import 'package:dartz/dartz.dart';

import '../../../lib.dart';

class ClientJobsRepositoryImpl implements ClientJobsRepository {
  final ClientJobsApi api;
  final NetworkInfo networkInfo;

  ClientJobsRepositoryImpl(this.api, this.networkInfo);
  @override
  Future<Either<Failure, bool>> createJob(Map<String, dynamic> data) async {
    final isConnected = await networkInfo.isConnected;
    if (!isConnected) {
      return Left(Failure.noConnection());
    }
    try {
      final response = await api.clientCreateJobs(data);
      if (!response.success) {
        return Left(Failure.serverError(response.message));
      }
      return Right(response.data!);
    } catch (e) {
      return Left(Failure.parseFromException(e));
    }
  }

  @override
  Future<Either<Failure, GetDetailJobsResponse>> getDetailJobs(
      String jobId) async {
    final isConnected = await networkInfo.isConnected;
    if (!isConnected) {
      return Left(Failure.noConnection());
    }
    try {
